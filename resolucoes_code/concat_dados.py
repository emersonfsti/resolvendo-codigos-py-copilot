# Nome do Desafio: Concatenação de Strings em Python
# Descrição: 
# Este desafio consiste em concatenar duas strings predefinidas 
# em uma única string. As strings são: "Utilizando as Ferramentas 
# do Github" e "para Solucionar Algoritmos em Python". O objetivo 
# é demonstrar como manipular strings em Python.
# ============================================================

# Definindo as entradas
primeira_entrada = "Utilizando as Ferramentas do Github"
segunda_entrada = "para Solucionar Algoritmos em Python"

# Concatenando as duas entradas
resultado = primeira_entrada + " " + segunda_entrada

# Exibindo o resultado
print("A string concatenada é:", resultado)


# ============================================================
# Versão do mesmo codigo com opção de entrada de dados:
# ============================================================

# Recebendo os dados do usuário
primeira_entrada = input("Digite a primeira frase: ")
segunda_entrada = input("Digite a segunda frase: ")

# Concatenando as duas entradas
resultado = primeira_entrada + " " + segunda_entrada

# Exibindo o resultado
print("A string concatenada é:", resultado)
