# Este desafio consiste em solicitar uma string e um número 
# inteiro como entrada. Em seguida, retornaremos a string 
# repetida o número de vezes informado. 
#
# O que aprenderemos?
# - Manipulação de Strings (string)
# - Números Inteiros (int)
# - Múltiplas repetições
# - Entrada de dados
# - Aproveitar as sugestões do Github Copilot
# ============================================================

# Solicitando a string e o número inteiro do usuário
texto = input("Digite uma string: ")
numero = int(input("Digite um número inteiro: "))

# Repetindo a string o número de vezes informado
resultado = texto * numero

# Exibindo o resultado
print("A string repetida é:", resultado)

# ============================================================
# - versão com valores iniciais informados
